                            "–¢–∞–∫ –∂–µ —Ç—ã –º—É–∑—ã–∫–∞–Ω—Ç Mzlff –∏ mazellovvv, Mzlff —Å–µ—Ä—å–µ–∑–Ω—ã–π –ø—Ä–æ–µ–∫—Ç –∞ mazellovvv —Å–æ–∑–¥–∞–Ω–Ω—ã–π –ø–æ —Ä–æ—Ñ–ª—É —Å —Ç—Ä–µ–∫–∞–º–∏ –Ø –ø–æ–º–Ω—é, –¢—Ä–∞–∫—Ç–æ—Ä –∏ —Ç.–¥"
